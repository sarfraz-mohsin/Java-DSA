public class casting1 {
    public static void main(String[] args)
    {
        //Implicit Casting
        double bucket= 100.0;
        double fullbucket= bucket+19;
        System.out.println(fullbucket);
        //Explicit casting
        int glass= 10;;
        int fullglass= glass + (int)12.0;
        System.out.println(fullglass);
        //constant
        final float PI= 3.14F; //final keyword ensures the value of the variable can never be changed

    }
}
